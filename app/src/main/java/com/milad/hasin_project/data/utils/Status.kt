package com.milad.hasin_project.data.utils

/**
 * The `Status` enum represents the different states of a network operation.
 */
enum class Status {
    /**
     * Indicates a successful network operation.
     */
    SUCCESS,

    /**
     * Indicates an error in the network operation.
     */
    ERROR,

    /**
     * Indicates that the network operation is in progress or loading.
     */
    LOADING
}